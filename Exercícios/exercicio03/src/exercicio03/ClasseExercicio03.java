package exercicio03;

public class ClasseExercicio03 {

	public static void main(String[] args) {
		int A, B, C, D, DIF;
		A=5;
		B=6;
		C=7;
		D=8;
		DIF = (A*B - C*D);
		
		System.out.printf("A diferencia é: %d\n", DIF);

		int A1, B1, C1, D1, DIF1;
		A1=5;
		B1=6;
		C1=-7;
		D1=8;
		DIF1 = (A1*B1 - C1*D1);
		
		System.out.printf("A diferencia é: %d\n", DIF1);
	}

}
